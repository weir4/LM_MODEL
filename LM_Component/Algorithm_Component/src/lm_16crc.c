/*******************************************************************************
函数名称： lm_crc16_mpdbus
功能描述： 使用查表法计算CRC-16/MODBUS校验值  
输入参数： data   - 指向待计算数据的指针，不能为NULL
          length - 待计算数据的长度，单位为字节
输出参数： 无
返 回 值： uint16_t - 计算得到的CRC-16校验值
其它说明： 1. 采用CRC-16/MODBUS标准：多项式0x8005，初始值0xFFFF
          2. 输入数据不进行位反转，输出结果不进行位反转
          3. 使用预先计算的查表表提高计算效率
          4. 适用于MODBUS通信协议等工业标准应用
修改日期      版本号          修改人            修改内容
-----------------------------------------------------------------------------
2025/09/30     V1.00          Qiguo_Cui          创建
*******************************************************************************/
uint16_t lm_crc16_modbus(const uint8_t *data, size_t length) 
{
    uint16_t crc = 0xFFFF;  // CRC初始值，按照MODBUS标准设置为0xFFFF
    
    // 遍历数据缓冲区中的每个字节
    for (size_t i = 0; i < length; i++) 
    {
        // 计算查表索引：当前CRC值的低8位与数据字节进行异或操作
        uint8_t index = (crc ^ data[i]) & 0xFF;
        
        // 通过查表更新CRC值：将CRC右移8位后与查表结果进行异或
        crc = (crc >> 8) ^ crc16_table[index];
    }
    
    // 返回最终的CRC校验值
    return crc;
}

/*******************************************************************************
函数名称： lm_get_crcbytes
功能描述： 将16位CRC值分解为两个字节，采用低字节优先的发送顺序  
输入参数： crc       - 16位的CRC校验值
输出参数： high_byte - 指向存储高字节的指针，不能为NULL
          low_byte  - 指向存储低字节的指针，不能为NULL  
返 回 值： 无
其它说明： 1. 按照低字节优先的通信协议要求进行字节分解
          2. 高字节存储在crc的低8位，低字节存储在crc的高8位
          3. 适用于MODBUS等要求先发送低字节的通信协议
          4. 使用时需确保输出参数指针有效
修改日期      版本号          修改人            修改内容
-----------------------------------------------------------------------------
2025/09/30     V1.00          Qiguo_Cui          创建
*******************************************************************************/
void lm_get_crcbytes(uint16_t crc, uint8_t *high_byte, uint8_t *low_byte) 
{
    // 对于先发送低字节再发送高字节的通信方式
    *high_byte = crc & 0xFF;        // 提取CRC值的低8位作为高字节（先发送）
    *low_byte = (crc >> 8) & 0xFF;  // 提取CRC值的高8位作为低字节（后发送）
}

